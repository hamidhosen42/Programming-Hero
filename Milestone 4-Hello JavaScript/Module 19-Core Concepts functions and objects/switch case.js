var color = "red";

if (color == "white") {
  console.log("color is white");
} else if (color == "yellow") {
  console.log("colors is yellow");
} else if (color == "green") {
  console.log("colors is green");
} else if (color == "pink") {
  console.log("colors is pink");
} else if (color == "red") {
  console.log("color is red");
} else {
  console.log("others color");
}

//-----------switch case-------------

switch (color) {
  case "white":
    console.log("colors is white");
    break;
  case "green":
    console.log("colors is green");
    break;
  case "yellow":
    console.log("colors is yellow");
    break;
  case "red":
    console.log("colors is red");
    break;
  default:
    console.log("colors s black");
}